#include "cub3d.h"

float	dist(float n, float d)
{
	if ((int)(n) == 1)
	{
		if (0 != (ceilf(d) - d))
			return (ceilf(d) - d);
		return (1);
	}
	if (0 != (d - truncf(d)))
	{
		return (d - truncf(d));
	}
	return (1);
}
